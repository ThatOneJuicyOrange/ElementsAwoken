        public override void FindFrame(int frameHeight)
        {
            if (npc.life < npc.lifeMax / 2)
            {
                npc.frame.Y = frameHeight * 1;
            }
            else
            {
                npc.frame.Y = 0;
            }
        }
        public override void ScaleExpertStats(int numPlayers, float bossLifeScale)
        {
            npc.damage = 140;
            npc.lifeMax = 100000;
            if (MyWorld.awakenedMode)
            {
                npc.lifeMax = 100000;
                npc.damage = 160;
                npc.defense = 30;
            }
        }

